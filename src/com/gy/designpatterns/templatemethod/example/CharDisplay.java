package com.gy.designpatterns.templatemethod.example;

public class CharDisplay extends AbstractDisplay{

    private Character ch;

    public CharDisplay(Character ch) {
        this.ch = ch;
    }

    @Override
    public void open() {
        System.out.print("<<");
    }

    @Override
    public void print() {
        System.out.print(ch);
    }

    @Override
    public void close() {
        System.out.println(">>");
    }
}
